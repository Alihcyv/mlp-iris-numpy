    "import torch\n",
    "import torch.nn as nn\n",
    "import torch.optim as optim\n",
    "from torch.utils.data import DataLoader, TensorDataset\n",
    "from sklearn import datasets\n",
    "from sklearn.model_selection import train_test_split\n",
    "from sklearn.preprocessing import StandardScaler\n",
    "\n",
    "# 1. Loading Data \n",
    "iris = datasets.load_iris()\n",
    "X = iris.data\n",
    "y = iris.target\n",
    "\n",
    "# split into train and test\n",
    "X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.2)\n",
    "\n",
    "# Standardizing data\n",
    "scaler = StandardScaler()\n",
    "X_train = scaler.fit_transform(X_train)\n",
    "X_test = scaler.transform(X_test)  # only transforem\n",
    "\n",
    "# Convert Numpy arrays to Pytorch Tensor\n",
    "# X is float32, y is int64 (LongTensor)\n",
    "X_train_tensor = torch.tensor(X_train, dtype=torch.float32)\n",
    "y_train_tensor = torch.tensor(y_train, dtype=torch.int64)\n",
    "X_test_tensor = torch.tensor(X_test, dtype=torch.float32)\n",
    "y_test_tensor = torch.tensor(y_test, dtype=torch.int64)\n",
    "\n",
    "# Create Dataloader (Handle batches and shuffling automatically)\n",
    "train_dataset = TensorDataset(X_train_tensor, y_train_tensor)\n",
    "train_loader = DataLoader(train_dataset, batch_size=16, shuffle=True, drop_last=True)\n",
    "\n",
    "# 2. Define the Neural Network Architecture\n",
    "class IrisNet(nn.Module):\n",
    "    def __init__(self, input_dim, h_dim, out_dim):\n",
    "        super(IrisNet, self).__init__()\n",
    "        # nn.Linear automatically initializes weights and biases\n",
    "        self.fc1 = nn.Linear(input_dim, h_dim)\n",
    "        self.relu = nn.ReLU()\n",
    "        self.fc2 = nn.Linear(h_dim, out_dim)\n",
    "\n",
    "    def forward(self, x):\n",
    "        x = self.fc1(x)\n",
    "        x = self.relu(x)\n",
    "        x = self.fc2(x)\n",
    "        # Note: No Softmax here!\n",
    "        # nn.CrossEntropyloss in Pytorch combines Softmax and Loss in one function\n",
    "        return x\n",
    "\n",
    "# 3 Initialize Model, Loss, and Optimizer\n",
    "model = IrisNet(input_dim=4, h_dim=5, out_dim=3)\n",
    "criterion = nn.CrossEntropyLoss()  # loss fnction\n",
    "optimizer = optim.SGD(model.parameters(), lr=0.05)\n",
    "\n",
    "# 4. Training Looop\n",
    "num_epochs=100\n",
    "\n",
    "for epoch in range(num_epochs):\n",
    "    model.train() \n",
    "    epoch_losses = 0\n",
    "\n",
    "    for X_batch, y_batch in train_loader:\n",
    "        # Forward Pass\n",
    "        outputs = model(X_batch)\n",
    "        loss = criterion(outputs, y_batch)\n",
    "\n",
    "        # Backward Pass \n",
    "        optimizer.zero_grad() # Clear previous gradients\n",
    "        loss.backward()       # Automatically calculate all derivatives (dW, dB)\n",
    "\n",
    "        # Update Weights\n",
    "        optimizer.step()      # W = W - lr * dW \n",
    "\n",
    "        epoch_losses += loss.item()\n",
    "\n",
    "    if (epoch+1) % 10 == 0:\n",
    "        model.eval() # Set model to evaluation mode\n",
    "        with torch.no_grad():\n",
    "            train_preds = model(X_train_tensor)  # count accuracy on train\n",
    "            train_acc = (torch.argmax(train_preds, dim=1)==y_train_tensor).float().mean() \n",
