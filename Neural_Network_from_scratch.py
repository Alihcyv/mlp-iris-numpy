    "import numpy as np\n",
    "from sklearn import datasets\n",
    "\n",
    "# 1. Initialize hyperparameters\n",
    "input_dim, h_dim, out_dim = 4, 10, 3  # Increased h_dim for better capacity\n",
    "batch_size = 16\n",
    "num_epoch = 100\n",
    "alpha = 0.05\n",
    "\n",
    "# 2. Load and prepare dataset\n",
    "iris = datasets.load_iris()\n",
    "X = iris.data\n",
    "y = iris.target\n",
    "\n",
    "# Neural networks converge much faster when input features are on the same scale.\n",
    "X = (X - np.mean(X, axis=0)) / np.std(X, axis=0)\n",
    "\n",
    "def to_one_hot(y, out_dim):\n",
    "    return np.eye(out_dim)[y]\n",
    "\n",
    "Y_oh = to_one_hot(y, out_dim)\n",
    "\n",
    "# Initial shuffle and split\n",
    "indices = np.arange(len(X))\n",
    "np.random.shuffle(indices)\n",
    "X, Y_oh = X[indices], Y_oh[indices]\n",
    "\n",
    "X_train, y_train = X[:120], Y_oh[:120]\n",
    "X_test, y_test = X[120:], Y_oh[120:]\n",
    "\n",
    "# 3. Initialize weights and biases\n",
    "# Use He Initialization: np.sqrt(2/n) helps prevent vanishing/exploding gradients.\n",
    "W1 = np.random.randn(input_dim, h_dim) * np.sqrt(2/input_dim)\n",
    "B1 = np.zeros((1, h_dim))\n",
    "W2 = np.random.randn(h_dim, out_dim) * np.sqrt(2/h_dim)\n",
    "B2 = np.zeros((1, out_dim))\n",
    "\n",
    "def ReLU(X):\n",
    "    return np.maximum(X, 0)\n",
    "\n",
    "def softmax(X):\n",
    "    # axis=1 is CRITICAL for mini-batches to normalize across classes, not samples.\n",
    "    exps = np.exp(X - np.max(X, axis=1, keepdims=True))\n",
    "    return exps / np.sum(exps, axis=1, keepdims=True)\n",
    "\n",
    "def loss(Y, Z):\n",
    "    # Cross-Entropy loss: we add 1e-15 to avoid log(0) which results in NaN.\n",
    "    return np.mean(-np.sum(Y * np.log(Z + 1e-15), axis=1))\n",
    "\n",
    "def feedforward(W1, W2, B1, B2, X):\n",
    "    A1 = X @ W1 + B1\n",
    "    H1 = ReLU(A1)\n",
    "    A2 = H1 @ W2 + B2\n",
    "    Z = softmax(A2)\n",
    "    return A1, H1, A2, Z\n",
    "\n",
    "def Backward(A1, H1, A2, Z, Y, X):\n",
    "    m = X.shape[0] # Use dynamic batch size (last batch might be smaller)\n",
    "    \n",
    "    dE_dA2 = Z - Y         \n",
    "    dE_dW2 = (H1.T @ dE_dA2) / m  # CRITICAL: Average gradient by dividing by batch size m\n",
    "    dE_dB2 = np.sum(dE_dA2, axis=0, keepdims=True) / m      \n",
    "    \n",
    "    dE_dH1 = dE_dA2 @ W2.T   \n",
    "    dE_dA1 = dE_dH1 * (A1 > 0) # ReLU derivative\n",
    "    \n",
    "    dE_dW1 = (X.T @ dE_dA1) / m\n",
    "    dE_dB1 = np.sum(dE_dA1, axis=0, keepdims=True) / m       \n",
    "    return dE_dW2, dE_dW1, dE_dB2, dE_dB1\n",
    "\n",
    "def Update(dW2, dW1, dB2, dB1, W1, W2, B1, B2):\n",
    "    W1 -= alpha * dW1\n",
    "    W2 -= alpha * dW2\n",
    "    B1 -= alpha * dB1\n",
    "    B2 -= alpha * dB2\n",
    "    return W1, W2, B1, B2\n",
    "\n",
    "def predict(X):\n",
    "    _, _, _, Z = feedforward(W1, W2, B1, B2, X)\n",
    "    return np.argmax(Z, axis=1)\n",
    "\n",
    "def accuracy(x_data, y_data):\n",
    "    prediction = predict(x_data)\n",
    "    y_true = np.argmax(y_data, axis=1)\n",
    "    return np.mean(prediction == y_true)\n",
    "\n",
    "# 4. Training Loop\n",
    "for epoch in range(num_epoch):\n",
    "    # Shuffle training data every epoch to improve generalization\n",
    "    indices = np.arange(len(X_train))\n",
    "    np.random.shuffle(indices)\n",
    "    X_train, y_train = X_train[indices], y_train[indices]\n",
    "    \n",
    "    epoch_losses = [] # Reset loss list every epoch\n",
    "      \n",
    "    for i in range(0, len(X_train), batch_size):\n",
    "        X_sample = X_train[i:i+batch_size]\n",
    "        Y_sample = y_train[i:i+batch_size]\n",
    "        \n",
    "        # Forward -> Backward -> Update\n",
    "        A1, H1, A2, Z = feedforward(W1, W2, B1, B2, X_sample)\n",
    "        E = loss(Y_sample, Z)\n",
    "        dW2, dW1, dB2, dB1 = Backward(A1, H1, A2, Z, Y_sample, X_sample)\n",
    "        W1, W2, B1, B2 = Update(dW2, dW1, dB2, dB1, W1, W2, B1, B2)\n",
    "        \n",
    "        epoch_losses.append(E)\n",
    "\n",
    "    if (epoch + 1) % 10 == 0:\n",
    "        acc_tr = accuracy(X_train, y_train)\n",
    "        acc_ts = accuracy(X_test, y_test)\n",
    "        print(f\"Epoch: {epoch+1:3} | Loss: {np.mean(epoch_losses):.4f} | Acc Train: {acc_tr:.4f} | Acc Test: {acc_ts:.4f}\")"
